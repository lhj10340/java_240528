package Day03_study;

public class IfEx03 {

	public static void main(String[] args) {
		// else if문 예제.
		
		int num = 0;
		
		if (num == 0) {
			System.out.println(" 0 입니다. ");
		}
		else if ( num > 0 ) {
			System.out.println(" 양수 입니다. ");
		}
		else {
			System.out.println(" 음수 입니다. ");
		}
		
	}

}
