package Day04_study;

public class ForEx01 {

	public static void main(String[] args) {
		
		// Hello world 를 5번 출력하는 반복문.
		// 반복문 코딩 시, 까먹지말고 '규칙성' '반복횟수' '반복문 종료 후' 를 꼭 적자.
		
		/* 반복횟수 : 5회
		 * 규칙성 : Hello world 출력
		 * 반복문 종료 후 : 없음.
		 * */
		
		for( int i = 0; i < 5 ; i++ ) {
			System.out.println("Hello World");
		}
		
	}

}
